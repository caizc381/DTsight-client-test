package com.tijiantest.model.item;

import com.tijiantest.util.key.PrimaryKey;

public class LimitItemKey implements PrimaryKey{
	
	private Integer itemId;
	
	public LimitItemKey(Integer itemId){
		this.itemId = itemId;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((itemId == null) ? 0 : itemId.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		LimitItemKey other = (LimitItemKey) obj;
		if (itemId == null) {
			if (other.itemId != null)
				return false;
		} else if (!itemId.equals(other.itemId))
			return false;
		return true;
	}
	
	
}
